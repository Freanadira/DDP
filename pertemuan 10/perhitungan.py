import math
def tambah(bil1, bil2):
    hasil = bil1+bil2
    print('hasil tambah dari',bil1,'+',bil2,'=',hasil)

def kurang(bil1, bil2):
    hasil = bil1-bil2
    print('hasil pengurangan dari',bil1,'-',bil2,'=',hasil)

def kali(bil1, bil2):
    hasil = bil1 * bil2
    print('hasil perkalian dari',bil1,'*',bil2,'=',hasil)

def bagi(bil1, bil2):
    hasil = bil1 / bil2
    print('hasil pembagian dari',bil1,'/',bil2,'=',hasil)

def pangkat(bil1, bil2):
    hasil = math.pow(bil1, bil2)
    print('hasil pemangkatan dari',bil1,'^',bil2,'=',hasil)

def akar(bil1):
    hasil = math.sqrt(bil1)
    print('hasil dari akar',bil1,'=',hasil)

def log(bil1):
    hasil = math.log(bil1)
    print('hasil dari log',bil1,'=',hasil)

def sin(bil1):
    hasil = math.sin(bil1)
    print('hasil dari sin',bil1,'=',hasil)

def cos(bil1):
    hasil = math.cos(bil1)
    print('hasil dari cos',bil1,'=',hasil)

def tan(bil1):
    hasil = math.tan(bil1)
    print('hasil dari tan',bil1, '=',hasil)

